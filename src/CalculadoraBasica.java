public class CalculadoraBasica {
    public double sumar(int a, int b){
        return a+b;
    }
    public double restar (int a, int b){
        return a-b;
    }
    public double dividir (int a, int b){
        return a/b;
    }
    public double multiplicar (int a, int b){
        return a*b;
    }
}
